// LoreGUI "Host a server" configuration screenshots.
//
// Captures the real ServiceSetup / AdvancedServerConfig components (the 54-field
// "Host a server" surface from SBAI-4075) for the website + README docs. Renders
// the built frontend (frontend/dist, served by `vite preview`) in headless
// Chromium via Playwright and drives the dev-only harness route
// (`?harness=server-config`, see App.tsx) so the config surface renders in
// isolation without walking the whole onboarding wizard.
//
// Like scripts/visual-test.mjs, the Tauri backend is stubbed: the only commands
// this surface calls are `host_server_status` (best-effort, returns "not
// running") and `host_server_render_config` (the "View generated config" TOML
// preview). The stub returns a TOML string that mirrors what the real Rust
// renderer (src-tauri/src/server_host.rs::render_config_toml) emits, so the
// preview screenshots are faithful.
//
// Usage:
//   BASE_URL=http://localhost:4173 node scripts/server-config-shots.mjs
//
// Output: PNGs in $OUT_DIR (default website/public/screenshots/server-config).

import { chromium } from "playwright";
import { mkdirSync } from "node:fs";

const BASE_URL = process.env.BASE_URL || "http://localhost:4173";
const OUT_DIR =
  process.env.OUT_DIR || "website/public/screenshots/server-config";
const VIEWPORT = { width: 1280, height: 900 };

mkdirSync(OUT_DIR, { recursive: true });

// TOML the stubbed `host_server_render_config` returns. Two variants keyed by a
