function installTauriStub() {
  let expertPreview = false;
  // The page flips this before asking for a preview.
  // eslint-disable-next-line no-undef
  window.__SC_EXPERT_PREVIEW__ = (v) => {
    expertPreview = Boolean(v);
  };
  const BASIC = `__BASIC_TOML__`;
  const EXPERT = `__EXPERT_TOML__`;
  // eslint-disable-next-line no-undef
  window.__TAURI_INTERNALS__ = {
    transformCallback(cb) {
      const id = Math.floor(Math.random() * 1e9);
      // eslint-disable-next-line no-undef
      window[`_tauri_cb_${id}`] = cb;
      return id;
    },
    unregisterCallback() {},
    convertFileSrc(p) {
      return p;
    },
    async invoke(cmd) {
      if (cmd === "host_server_render_config") {
        return expertPreview ? EXPERT : BASIC;
      }
      if (cmd === "host_server_status") {
        return { running: false, url: "", advertisedUrl: null };
      }
      return null;
    },
    metadata: {
      currentWindow: { label: "main" },
      currentWebview: { label: "main" },
    },
  };
  // eslint-disable-next-line no-undef
  window.__TAURI__ = window.__TAURI__ || {};
}

// Build the init script with the TOML payloads inlined (addInitScript can't
// close over module scope, so we string-substitute the literals). Wrap the
// function in an IIFE so it actually runs when injected.
const initScriptSource = `(${installTauriStub
  .toString()
  .replace("`__BASIC_TOML__`", JSON.stringify(BASIC_TOML))
  .replace("`__EXPERT_TOML__`", JSON.stringify(EXPERT_TOML))})();`;

async function shoot(page, name, locator) {
  const file = `${OUT_DIR}/${name}.png`;
  const target = locator ?? page;
  await target.screenshot({ path: file });
  console.log(`  shot: ${file}`);
}

async function expandAll(page) {
  // Open every collapsible Advanced section so a full-card screenshot shows them.
  const toggles = page.locator(".server-config-section-toggle");
  const n = await toggles.count();
  for (let i = 0; i < n; i++) {
    const t = toggles.nth(i);
    if ((await t.getAttribute("aria-expanded")) !== "true") {
      await t.click();
      await page.waitForTimeout(80);
    }
  }
}

async function openSection(page, titleRe) {
  const toggle = page
    .locator(".server-config-section-toggle", { hasText: titleRe })
    .first();
  if ((await toggle.getAttribute("aria-expanded")) !== "true") {
    await toggle.click();
    await page.waitForTimeout(120);
  }
  return toggle.locator("xpath=ancestor::div[contains(@class,'server-config-section')][1]");
}

async function collapseAll(page) {
  const toggles = page.locator(".server-config-section-toggle");
  const n = await toggles.count();
  for (let i = 0; i < n; i++) {
    const t = toggles.nth(i);
    if ((await t.getAttribute("aria-expanded")) === "true") {
      await t.click();
      await page.waitForTimeout(60);
    }
  }
}

async function run() {
  const browser = await chromium.launch();
  const ctx = await browser.newContext({ viewport: VIEWPORT, colorScheme: "dark" });
  const page = await ctx.newPage();
  page.on("pageerror", (e) => console.warn(`  [pageerror] ${e.message}`));
  await page.addInitScript({ content: initScriptSource });

  const url = `${BASE_URL}/?harness=server-config`;
  await page.goto(url, { waitUntil: "networkidle" });
  await page.waitForSelector(".onboarding-card", { timeout: 15000 });
  await page.waitForTimeout(400);

  const card = page.locator(".onboarding-card").first();

  // 1. Basic mode — the default surface (store dir + Basic/Expert tabs).
  await shoot(page, "01-basic", card);

  // 2. Basic mode "View generated config" — the minimal local.toml preview.
  await page.evaluate(() => window.__SC_EXPERT_PREVIEW__(false));
  await page
    .locator("button", { hasText: /^View generated config$/ })
    .first()
    .click();
  await page.waitForSelector("pre[aria-label='Generated config TOML']", {
    timeout: 5000,
  });
  await page.waitForTimeout(200);
  await shoot(page, "09-toml-preview-basic", card);
  // Hide the preview again before switching modes.
  await page.locator(".server-config-preview button", { hasText: /^Hide$/ }).first().click();
  await page.waitForTimeout(150);

  // Switch to Expert mode to reveal the full advanced surface.
  await page.locator("button[role='tab']", { hasText: /^Expert$/ }).click();
  await page.waitForSelector(".server-config-advanced", { timeout: 5000 });
  await page.waitForTimeout(200);

  // 3. Expert mode overview — all advanced sections expanded in one tall shot.
  await expandAll(page);
  await page.waitForTimeout(200);
  await shoot(page, "02-expert-overview", card);

  // 4..N — each advanced section on its own (collapse the rest for a focused
  // shot). Section titles come straight from AdvancedServerConfig.tsx.
  const sections = [
    ["03-network", /Network: bind host, QUIC, gRPC, HTTP/],
    ["04-storage", /Storage: local store tuning & lock store/],
    ["05-topology", /Topology & replication/],
    ["06-telemetry", /Telemetry/],
    ["07-runtime", /Runtime \(Tokio\)/],
    ["08-features", /Features/],
  ];
  for (const [name, re] of sections) {
    await collapseAll(page);
    const section = await openSection(page, re);
    await page.waitForTimeout(150);
    await section.scrollIntoViewIfNeeded();
    await shoot(page, name, section);
  }

  // Also a focused shot of the smaller sections grouped (notifications + timeouts)
  // by opening them together.
  await collapseAll(page);
  await openSection(page, /Notifications/);
  await openSection(page, /Shutdown timeouts/);
  await page.waitForTimeout(150);
  await shoot(page, "10-notifications-timeouts", card);

  // 5. Expert "View generated config" — a customized config preview. Type a few
  // representative values so the surface visibly reflects customization, then
  // request the expert TOML preview.
  await collapseAll(page);
  await openSection(page, /Network: bind host, QUIC, gRPC, HTTP/);
  await page.fill("#sc-bind-host", "0.0.0.0");
  await page.fill("#sc-quic-idle", "60000");
  await page.fill("#sc-quic-listeners", "16");
  await page.waitForTimeout(120);
  await page.evaluate(() => window.__SC_EXPERT_PREVIEW__(true));
  await page
    .locator("button", { hasText: /^View generated config$/ })
    .first()
    .click();
  await page.waitForSelector("pre[aria-label='Generated config TOML']", {
    timeout: 5000,
  });
  await page.waitForTimeout(200);
  await shoot(page, "11-toml-preview-expert", card);
  // A focused, readable crop of just the customized TOML preview block.
  const preview = page.locator(".server-config-preview").first();
  await preview.scrollIntoViewIfNeeded();
  await page.waitForTimeout(150);
  await shoot(page, "12-toml-preview-expert-detail", preview);

  await ctx.close();
  await browser.close();
  console.log("\nDone. Screenshots in", OUT_DIR);
}

run().catch((e) => {
  console.error(e);
  process.exit(1);
});
